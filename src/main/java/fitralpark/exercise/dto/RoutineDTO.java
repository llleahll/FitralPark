package fitralpark.exercise.dto;



public class RoutineDTO {
	private String routineNo;
    private String routineName;
    private String routineCategoryName;
    private String exerciseCategories;
    private String exerciseParts;
    private String totalCalories;
    private String creationDate;
    private String memberNickname;
    private String views;
	public String getRoutineNo() {
		return routineNo;
	}
	public void setRoutineNo(String routineNo) {
		this.routineNo = routineNo;
	}
	public String getRoutineName() {
		return routineName;
	}
	public void setRoutineName(String routineName) {
		this.routineName = routineName;
	}
	public String getRoutineCategoryName() {
		return routineCategoryName;
	}
	public void setRoutineCategoryName(String routineCategoryName) {
		this.routineCategoryName = routineCategoryName;
	}
	public String getExerciseCategories() {
		return exerciseCategories;
	}
	public void setExerciseCategories(String exerciseCategories) {
		this.exerciseCategories = exerciseCategories;
	}
	public String getExerciseParts() {
		return exerciseParts;
	}
	public void setExerciseParts(String exerciseParts) {
		this.exerciseParts = exerciseParts;
	}
	public String getTotalCalories() {
		return totalCalories;
	}
	public void setTotalCalories(String totalCalories) {
		this.totalCalories = totalCalories;
	}
	public String getCreationDate() {
		return creationDate;
	}
	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}
	public String getMemberNickname() {
		return memberNickname;
	}
	public void setMemberNickname(String memberNickname) {
		this.memberNickname = memberNickname;
	}
	public String getViews() {
		return views;
	}
	public void setViews(String views) {
		this.views = views;
	}
    
    
}

